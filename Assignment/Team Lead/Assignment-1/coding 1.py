

list=[10,20,30,40,50]
print(list)

#insertion
list.insert(5,100)
print(list)

#deletion of first occurence of integer
del list[0]
print(list)

#insertion at end of list
list.append(-15)
print(list)

#sorting
list.sort()
print(list)

#popping last element
list.pop()
print(list)

#reverse the list
list.reverse()
print(list)

